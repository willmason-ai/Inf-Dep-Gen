// ============================================================================
// Infrastructure Deployment Generator — Tag Script Generator
// ============================================================================
// Generates PowerShell scripts to apply required tags to VMs and their disks.
// Uses Update-AzTag -Operation Merge for non-destructive tagging.
// ============================================================================

import config from '../config/index.js';
import { getDiskPurposeSlug } from '../models/server-spec.js';

// ---------------------------------------------------------------------------
// Build the standard 6 tags for a server
// ---------------------------------------------------------------------------
function buildStandardTags(spec) {
  return {
    Environment: spec.tags?.Environment || 'Prod',
    Owner: spec.tags?.Owner || 'MISSING-TAG-VALUE',
    'Cost Center': spec.tags?.['Cost Center'] || 'MISSING-TAG-VALUE',
    Application: spec.tags?.Application || 'MISSING-TAG-VALUE',
    ...(spec.tags || {}),
  };
}

// ---------------------------------------------------------------------------
// Format a tag hashtable for PowerShell
// ---------------------------------------------------------------------------
function formatPsHashtable(tags, indent = '    ') {
  const lines = ['@{'];
  const entries = Object.entries(tags);
  for (let i = 0; i < entries.length; i++) {
    const [key, value] = entries[i];
    // PowerShell hashtable syntax — escape single quotes in values
    const escapedValue = String(value).replace(/'/g, "''");
    const escapedKey = key.includes(' ') ? `'${key}'` : key;
    lines.push(`${indent}    ${escapedKey} = '${escapedValue}'`);
  }
  lines.push(`${indent}}`);
  return lines.join('\n');
}

// ---------------------------------------------------------------------------
// Main generator: produce PowerShell tag script from a server spec
// ---------------------------------------------------------------------------
export function generateTagScript(spec) {
  const standardTags = buildStandardTags(spec);
  const warnings = [];
  const isLab = config.environment === 'lab';
  const rgName = isLab
    ? config.profile.resourceGroups.eus2Compute
    : spec.resourceGroup;

  const lines = [];

  // Header
  lines.push('# ============================================================================');
  lines.push(`# Tag Script for ${spec.hostname}`);
  lines.push(`# Server: ${spec.role}`);
  lines.push('# ============================================================================');
  lines.push(`# Generated by Infrastructure Deployment Generator on ${new Date().toISOString()}`);
  lines.push('# Uses Update-AzTag -Operation Merge (non-destructive — preserves existing tags)');
  lines.push('# ============================================================================');
  lines.push('');
  lines.push('# Ensure Azure PowerShell module is loaded');
  lines.push('# Import-Module Az.Resources');
  lines.push('');

  // Variables
  lines.push(`$ResourceGroupName = "${rgName}"`);
  lines.push(`$VMName = "${spec.hostname}"`);
  lines.push('');

  // Standard tags hashtable
  lines.push('# Standard tags (applied to VM, NIC, and all disks)');
  lines.push(`$StandardTags = ${formatPsHashtable(standardTags, '')}`);
  lines.push('');

  // Tag the VM
  lines.push('# ============================================================================');
  lines.push('# Tag the Virtual Machine');
  lines.push('# ============================================================================');
  lines.push('$vm = Get-AzVM -ResourceGroupName $ResourceGroupName -Name $VMName');
  lines.push('Update-AzTag -ResourceId $vm.Id -Tag $StandardTags -Operation Merge');
  lines.push('Write-Host "Tagged VM: $VMName"');
  lines.push('');

  // Tag the NIC
  lines.push('# ============================================================================');
  lines.push('# Tag the Network Interface');
  lines.push('# ============================================================================');
  lines.push('$nicId = $vm.NetworkProfile.NetworkInterfaces[0].Id');
  lines.push('Update-AzTag -ResourceId $nicId -Tag $StandardTags -Operation Merge');
  lines.push('Write-Host "Tagged NIC for: $VMName"');
  lines.push('');

  // Tag each data disk
  lines.push('# ============================================================================');
  lines.push('# Tag Data Disks');
  lines.push('# ============================================================================');

  let lun = 0;

  if (spec.serverType === 'odb' && spec.volumeGroups) {
    for (const vg of spec.volumeGroups) {
      if (typeof vg.diskCount !== 'number') {
        warnings.push(`VolumeGroup "${vg.name}": diskCount is "${vg.diskCount}" — skipping tag generation for this group.`);
        continue;
      }
      for (let i = 0; i < vg.diskCount; i++) {
        const diskIndex = i + 1;
        const diskName = `${spec.hostname}-${vg.name}-disk${diskIndex}`;

        lines.push('');
        lines.push(`# ${diskName} (${vg.name}, LUN ${lun})`);
        lines.push(`$diskTags = $StandardTags.Clone()`);
        lines.push(`$diskTags['VolumeGroup'] = '${vg.name}'`);
        lines.push(`$diskTags['ServerName'] = '${spec.hostname}'`);
        lines.push(`$diskTags['DiskIndex'] = '${diskIndex}'`);
        lines.push(`$diskTags['LUN'] = '${lun}'`);
        lines.push(`$disk = Get-AzDisk -ResourceGroupName $ResourceGroupName -DiskName "${diskName}"`);
        lines.push('Update-AzTag -ResourceId $disk.Id -Tag $diskTags -Operation Merge');
        lines.push(`Write-Host "Tagged disk: ${diskName}"`);

        lun++;
      }
    }
  }

  if (spec.serverType === 'sql' && spec.diskGroups) {
    for (const dg of spec.diskGroups) {
      if (typeof dg.diskCount !== 'number') {
        warnings.push(`DiskGroup "${dg.purpose}": diskCount is "${dg.diskCount}" — skipping tag generation for this group.`);
        continue;
      }
      const count = dg.diskCount;
      const purposeSlug = getDiskPurposeSlug(dg.purpose);

      for (let i = 0; i < count; i++) {
        const diskIndex = i + 1;
        const diskName = `${spec.hostname}-${purposeSlug}-disk${diskIndex}`;

        lines.push('');
        lines.push(`# ${diskName} (${dg.purpose}, LUN ${lun})`);
        lines.push(`$diskTags = $StandardTags.Clone()`);
        lines.push(`$diskTags['DiskPurpose'] = '${dg.purpose}'`);
        lines.push(`$diskTags['ServerName'] = '${spec.hostname}'`);
        lines.push(`$diskTags['DiskIndex'] = '${diskIndex}'`);
        lines.push(`$diskTags['LUN'] = '${lun}'`);
        lines.push(`$disk = Get-AzDisk -ResourceGroupName $ResourceGroupName -DiskName "${diskName}"`);
        lines.push('Update-AzTag -ResourceId $disk.Id -Tag $diskTags -Operation Merge');
        lines.push(`Write-Host "Tagged disk: ${diskName}"`);

        lun++;
      }
    }
  }

  // Tag OS disk
  lines.push('');
  lines.push('# ============================================================================');
  lines.push('# Tag OS Disk');
  lines.push('# ============================================================================');
  lines.push('$osDiskName = $vm.StorageProfile.OsDisk.Name');
  lines.push('$osDisk = Get-AzDisk -ResourceGroupName $ResourceGroupName -DiskName $osDiskName');
  lines.push('$osDiskTags = $StandardTags.Clone()');
  lines.push(`$osDiskTags['ServerName'] = '${spec.hostname}'`);
  lines.push(`$osDiskTags['DiskPurpose'] = 'OSDisk'`);
  lines.push('Update-AzTag -ResourceId $osDisk.Id -Tag $osDiskTags -Operation Merge');
  lines.push('Write-Host "Tagged OS disk: $osDiskName"');
  lines.push('');

  // Summary
  lines.push('# ============================================================================');
  lines.push('Write-Host ""');
  lines.push(`Write-Host "Tagging complete for ${spec.hostname}"`);
  lines.push(`Write-Host "  VM + NIC + ${lun} data disk(s) + OS disk tagged"`);
  lines.push('');

  const script = lines.join('\n');

  const summary = {
    hostname: spec.hostname,
    serverType: spec.serverType,
    resourceGroup: rgName,
    standardTags,
    totalDataDisks: lun,
    totalResourcesTagged: lun + 3, // VM + NIC + OS disk + data disks
    warnings: warnings.length > 0 ? warnings : undefined,
  };

  return { script, summary, warnings };
}

export default { generateTagScript };
