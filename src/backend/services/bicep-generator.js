// Infrastructure Deployment Generator — Bicep / ARM Template Generator
// ============================================================================
// Generates Bicep and ARM JSON templates for the networking topology.
// ============================================================================

import { parseCidr } from './cidr-utils.js';

// ---------------------------------------------------------------------------
// Generate Bicep template
// ---------------------------------------------------------------------------

export function generateBicep(cfg, names = {}) {
  const lines = [];
  const summary = { resources: [] };

  const region = cfg.region || 'eastus2';
  const rg = cfg.resourceGroupName || 'rg-networking';
  const vnetName = names.hubVnet || cfg.hubVnet?.name || 'hub-vnet';
  const enabledSubnets = (cfg.subnets || []).filter(s => s.enabled !== false);
  const conn = cfg.connectivity || {};

  // Header
  lines.push('// ============================================================================');
  lines.push('// Networking Hub — Auto-generated by Infrastructure Deployment Generator');
  lines.push(`// Region: ${region}`);
  lines.push(`// Generated: ${new Date().toISOString()}`);
  lines.push('// ============================================================================');
  lines.push('');
  lines.push("targetScope = 'resourceGroup'");
  lines.push('');

  // Parameters
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('// Parameters');
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('');
  lines.push(`param location string = '${region}'`);
  lines.push('param tags object = {}');
  lines.push('');

  // Variables
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('// Variables');
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('');
  lines.push(`var vnetName = '${vnetName}'`);

  // NSG names for subnets that need them
  for (const subnet of enabledSubnets) {
    if (subnet.nsg) {
      const nsgName = names[`nsg-${subnet.id}`] || `${subnet.name || subnet.purpose}-nsg`;
      lines.push(`var nsg${capitalize(subnet.purpose)}Name = '${nsgName}'`);
    }
    if (subnet.routeTable) {
      const rtName = names[`rt-${subnet.id}`] || `${subnet.name || subnet.purpose}-rt`;
      lines.push(`var rt${capitalize(subnet.purpose)}Name = '${rtName}'`);
    }
  }

  if (conn.bastion?.enabled) {
    const bastionName = names.bastion || conn.bastion.name || 'bastion';
    lines.push(`var bastionName = '${bastionName}'`);
    lines.push(`var bastionPipName = '${bastionName}-pip'`);
  }

  if (conn.expressRouteGateway?.enabled) {
    const gwName = names.erGateway || conn.expressRouteGateway.name || 'er-gateway';
    lines.push(`var erGatewayName = '${gwName}'`);
    lines.push(`var erGatewayPipName = '${gwName}-pip'`);
  }

  if (conn.firewall?.enabled) {
    const fwName = names.firewall || conn.firewall.name || 'azure-firewall';
    lines.push(`var firewallName = '${fwName}'`);
    lines.push(`var firewallPipName = '${fwName}-pip'`);
    const policyName = names.firewallPolicy || conn.firewall.policyName || `${fwName}-policy`;
    lines.push(`var firewallPolicyName = '${policyName}'`);
  }

  lines.push('');

  // NSG resources
  for (const subnet of enabledSubnets) {
    if (!subnet.nsg) continue;

    const varPrefix = `nsg${capitalize(subnet.purpose)}`;
    lines.push('// ---------------------------------------------------------------------------');
    lines.push(`// NSG — ${subnet.name || subnet.purpose}`);
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('');
    lines.push(`resource ${varPrefix} 'Microsoft.Network/networkSecurityGroups@2023-11-01' = {`);
    lines.push(`  name: ${varPrefix}Name`);
    lines.push('  location: location');
    lines.push('  tags: tags');
    lines.push('  properties: {');
    lines.push('    securityRules: []');
    lines.push('  }');
    lines.push('}');
    lines.push('');
    summary.resources.push(`NSG: ${subnet.name || subnet.purpose}`);
  }

  // Route Table resources
  for (const subnet of enabledSubnets) {
    if (!subnet.routeTable) continue;

    const varPrefix = `rt${capitalize(subnet.purpose)}`;
    lines.push(`resource ${varPrefix} 'Microsoft.Network/routeTables@2023-11-01' = {`);
    lines.push(`  name: ${varPrefix}Name`);
    lines.push('  location: location');
    lines.push('  tags: tags');
    lines.push('  properties: {');
    lines.push('    disableBgpRoutePropagation: false');
    lines.push('    routes: []');
    lines.push('  }');
    lines.push('}');
    lines.push('');
    summary.resources.push(`Route Table: ${subnet.name || subnet.purpose}`);
  }

  // VNet with inline subnets
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('// Hub Virtual Network');
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('');
  lines.push("resource hubVnet 'Microsoft.Network/virtualNetworks@2023-11-01' = {");
  lines.push('  name: vnetName');
  lines.push('  location: location');
  lines.push('  tags: tags');
  lines.push('  properties: {');
  lines.push('    addressSpace: {');
  lines.push('      addressPrefixes: [');
  for (const space of (cfg.hubVnet?.addressSpaces || ['10.0.0.0/16'])) {
    lines.push(`        '${space}'`);
  }
  lines.push('      ]');
  lines.push('    }');
  lines.push('    subnets: [');

  for (let i = 0; i < enabledSubnets.length; i++) {
    const subnet = enabledSubnets[i];
    const subnetName = subnet.name || subnet.purpose;
    lines.push('      {');
    lines.push(`        name: '${subnetName}'`);
    lines.push('        properties: {');
    lines.push(`          addressPrefix: '${subnet.cidr}'`);

    if (subnet.nsg) {
      const varPrefix = `nsg${capitalize(subnet.purpose)}`;
      lines.push(`          networkSecurityGroup: {`);
      lines.push(`            id: ${varPrefix}.id`);
      lines.push('          }');
    }

    if (subnet.routeTable) {
      const varPrefix = `rt${capitalize(subnet.purpose)}`;
      lines.push(`          routeTable: {`);
      lines.push(`            id: ${varPrefix}.id`);
      lines.push('          }');
    }

    lines.push('        }');
    lines.push('      }');
  }

  lines.push('    ]');
  lines.push('  }');
  lines.push('}');
  lines.push('');
  summary.resources.push(`VNet: ${vnetName} (${enabledSubnets.length} subnets)`);

  // Bastion
  if (conn.bastion?.enabled) {
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('// Azure Bastion');
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('');
    lines.push("resource bastionPip 'Microsoft.Network/publicIPAddresses@2023-11-01' = {");
    lines.push('  name: bastionPipName');
    lines.push('  location: location');
    lines.push('  tags: tags');
    lines.push('  sku: {');
    lines.push("    name: 'Standard'");
    lines.push('  }');
    lines.push('  properties: {');
    lines.push("    publicIPAllocationMethod: 'Static'");
    lines.push('  }');
    lines.push('}');
    lines.push('');
    lines.push("resource bastion 'Microsoft.Network/bastionHosts@2023-11-01' = {");
    lines.push('  name: bastionName');
    lines.push('  location: location');
    lines.push('  tags: tags');
    lines.push('  sku: {');
    lines.push(`    name: '${conn.bastion.sku || 'Standard'}'`);
    lines.push('  }');
    lines.push('  properties: {');
    lines.push('    ipConfigurations: [');
    lines.push('      {');
    lines.push("        name: 'bastionIpConfig'");
    lines.push('        properties: {');
    lines.push('          subnet: {');
    lines.push("            id: hubVnet.properties.subnets[?name == 'AzureBastionSubnet'].id");
    lines.push('          }');
    lines.push('          publicIPAddress: {');
    lines.push('            id: bastionPip.id');
    lines.push('          }');
    lines.push('        }');
    lines.push('      }');
    lines.push('    ]');
    lines.push('  }');
    lines.push('}');
    lines.push('');
    summary.resources.push(`Bastion: ${conn.bastion.sku || 'Standard'}`);
  }

  // ExpressRoute Gateway
  if (conn.expressRouteGateway?.enabled) {
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('// ExpressRoute Virtual Network Gateway');
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('');
    lines.push("resource erGatewayPip 'Microsoft.Network/publicIPAddresses@2023-11-01' = {");
    lines.push('  name: erGatewayPipName');
    lines.push('  location: location');
    lines.push('  tags: tags');
    lines.push('  sku: {');
    lines.push("    name: 'Standard'");
    lines.push('  }');
    lines.push('  properties: {');
    lines.push("    publicIPAllocationMethod: 'Static'");
    lines.push('  }');
    lines.push('}');
    lines.push('');
    lines.push("resource erGateway 'Microsoft.Network/virtualNetworkGateways@2023-11-01' = {");
    lines.push('  name: erGatewayName');
    lines.push('  location: location');
    lines.push('  tags: tags');
    lines.push('  properties: {');
    lines.push("    gatewayType: 'ExpressRoute'");
    lines.push('    sku: {');
    lines.push(`      name: '${conn.expressRouteGateway.sku || 'ErGw3AZ'}'`);
    lines.push(`      tier: '${conn.expressRouteGateway.sku || 'ErGw3AZ'}'`);
    lines.push('    }');
    lines.push('    ipConfigurations: [');
    lines.push('      {');
    lines.push("        name: 'default'");
    lines.push('        properties: {');
    lines.push('          subnet: {');
    lines.push("            id: hubVnet.properties.subnets[?name == 'GatewaySubnet'].id");
    lines.push('          }');
    lines.push('          publicIPAddress: {');
    lines.push('            id: erGatewayPip.id');
    lines.push('          }');
    lines.push('        }');
    lines.push('      }');
    lines.push('    ]');
    lines.push('  }');
    lines.push('}');
    lines.push('');
    summary.resources.push(`ER Gateway: ${conn.expressRouteGateway.sku || 'ErGw3AZ'}`);
  }

  // ExpressRoute Connection
  if (conn.expressRouteConnection?.enabled && conn.expressRouteGateway?.enabled) {
    const connName = names.erConnection || conn.expressRouteConnection.name || 'er-connection';
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('// ExpressRoute Connection');
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('');
    lines.push("resource erConnection 'Microsoft.Network/connections@2023-11-01' = {");
    lines.push(`  name: '${connName}'`);
    lines.push('  location: location');
    lines.push('  tags: tags');
    lines.push('  properties: {');
    lines.push("    connectionType: 'ExpressRoute'");
    lines.push('    virtualNetworkGateway1: {');
    lines.push('      id: erGateway.id');
    lines.push('    }');
    if (conn.expressRouteConnection.circuitResourceId) {
      lines.push('    peer: {');
      lines.push(`      id: '${conn.expressRouteConnection.circuitResourceId}'`);
      lines.push('    }');
    }
    if (conn.expressRouteConnection.authorizationKey) {
      lines.push(`    authorizationKey: '${conn.expressRouteConnection.authorizationKey}'`);
    }
    lines.push('  }');
    lines.push('}');
    lines.push('');
    summary.resources.push('ER Connection');
  }

  // ExpressRoute Circuit (if planning new)
  if (conn.expressRouteCircuit?.planNew) {
    const circuitName = names.erCircuit || conn.expressRouteCircuit.name || 'er-circuit';
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('// ExpressRoute Circuit (new)');
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('');
    lines.push("resource erCircuit 'Microsoft.Network/expressRouteCircuits@2023-11-01' = {");
    lines.push(`  name: '${circuitName}'`);
    lines.push('  location: location');
    lines.push('  tags: tags');
    lines.push('  sku: {');
    lines.push(`    name: '${conn.expressRouteCircuit.sku || 'Standard'}_MeteredData'`);
    lines.push(`    tier: '${conn.expressRouteCircuit.sku || 'Standard'}'`);
    lines.push("    family: 'MeteredData'");
    lines.push('  }');
    lines.push('  properties: {');
    lines.push('    serviceProviderProperties: {');
    lines.push(`      serviceProviderName: '${conn.expressRouteCircuit.provider || 'TBD'}'`);
    lines.push(`      peeringLocation: '${conn.expressRouteCircuit.peeringLocation || 'TBD'}'`);
    lines.push(`      bandwidthInMbps: ${parseInt(conn.expressRouteCircuit.bandwidth) || 1000}`);
    lines.push('    }');
    lines.push('  }');
    lines.push('}');
    lines.push('');
    summary.resources.push(`ER Circuit: ${conn.expressRouteCircuit.provider || 'TBD'}`);
  }

  // Firewall
  if (conn.firewall?.enabled) {
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('// Azure Firewall');
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('');
    lines.push("resource firewallPip 'Microsoft.Network/publicIPAddresses@2023-11-01' = {");
    lines.push('  name: firewallPipName');
    lines.push('  location: location');
    lines.push('  tags: tags');
    lines.push('  sku: {');
    lines.push("    name: 'Standard'");
    lines.push('  }');
    lines.push('  properties: {');
    lines.push("    publicIPAllocationMethod: 'Static'");
    lines.push('  }');
    lines.push('}');
    lines.push('');
    lines.push("resource firewallPolicy 'Microsoft.Network/firewallPolicies@2023-11-01' = {");
    lines.push('  name: firewallPolicyName');
    lines.push('  location: location');
    lines.push('  tags: tags');
    lines.push('  properties: {');
    lines.push(`    threatIntelMode: '${conn.firewall.threatIntelMode || 'Alert'}'`);
    lines.push('    sku: {');
    lines.push(`      tier: '${conn.firewall.sku || 'Standard'}'`);
    lines.push('    }');
    lines.push('  }');
    lines.push('}');
    lines.push('');
    lines.push("resource firewall 'Microsoft.Network/azureFirewalls@2023-11-01' = {");
    lines.push('  name: firewallName');
    lines.push('  location: location');
    lines.push('  tags: tags');
    lines.push('  properties: {');
    lines.push('    sku: {');
    lines.push("      name: 'AZFW_VNet'");
    lines.push(`      tier: '${conn.firewall.sku || 'Standard'}'`);
    lines.push('    }');
    lines.push('    firewallPolicy: {');
    lines.push('      id: firewallPolicy.id');
    lines.push('    }');
    lines.push('    ipConfigurations: [');
    lines.push('      {');
    lines.push("        name: 'firewallIpConfig'");
    lines.push('        properties: {');
    lines.push('          subnet: {');
    lines.push("            id: hubVnet.properties.subnets[?name == 'AzureFirewallSubnet'].id");
    lines.push('          }');
    lines.push('          publicIPAddress: {');
    lines.push('            id: firewallPip.id');
    lines.push('          }');
    lines.push('        }');
    lines.push('      }');
    lines.push('    ]');
    lines.push('  }');
    lines.push('}');
    lines.push('');
    summary.resources.push(`Firewall: ${conn.firewall.sku || 'Standard'}`);
  }

  // Outputs
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('// Outputs');
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('');
  lines.push('output vnetId string = hubVnet.id');
  lines.push('output vnetName string = hubVnet.name');
  if (conn.expressRouteGateway?.enabled) {
    lines.push('output erGatewayId string = erGateway.id');
  }
  if (conn.bastion?.enabled) {
    lines.push('output bastionId string = bastion.id');
  }
  if (conn.firewall?.enabled) {
    lines.push('output firewallId string = firewall.id');
    lines.push('output firewallPrivateIp string = firewall.properties.ipConfigurations[0].properties.privateIPAddress');
  }
  lines.push('');

  return { bicep: lines.join('\n'), summary };
}

// ---------------------------------------------------------------------------
// Generate ARM JSON template
// ---------------------------------------------------------------------------

export function generateArmJson(cfg, names = {}) {
  const region = cfg.region || 'eastus2';
  const vnetName = names.hubVnet || cfg.hubVnet?.name || 'hub-vnet';
  const enabledSubnets = (cfg.subnets || []).filter(s => s.enabled !== false);
  const conn = cfg.connectivity || {};
  const summary = { resources: [] };

  const template = {
    $schema: 'https://schema.management.azure.com/schemas/2019-04-01/deploymentTemplate.json#',
    contentVersion: '1.0.0.0',
    metadata: {
      _generator: {
        name: 'Infrastructure Deployment Generator',
        version: '1.0.0',
        timestamp: new Date().toISOString(),
      },
    },
    parameters: {
      location: {
        type: 'string',
        defaultValue: region,
      },
      tags: {
        type: 'object',
        defaultValue: {},
      },
    },
    variables: {},
    resources: [],
    outputs: {},
  };

  // NSGs
  for (const subnet of enabledSubnets) {
    if (!subnet.nsg) continue;
    const nsgName = names[`nsg-${subnet.id}`] || `${subnet.name || subnet.purpose}-nsg`;
    template.resources.push({
      type: 'Microsoft.Network/networkSecurityGroups',
      apiVersion: '2023-11-01',
      name: nsgName,
      location: '[parameters(\'location\')]',
      tags: '[parameters(\'tags\')]',
      properties: { securityRules: [] },
    });
    summary.resources.push(`NSG: ${nsgName}`);
  }

  // Route Tables
  for (const subnet of enabledSubnets) {
    if (!subnet.routeTable) continue;
    const rtName = names[`rt-${subnet.id}`] || `${subnet.name || subnet.purpose}-rt`;
    template.resources.push({
      type: 'Microsoft.Network/routeTables',
      apiVersion: '2023-11-01',
      name: rtName,
      location: '[parameters(\'location\')]',
      tags: '[parameters(\'tags\')]',
      properties: {
        disableBgpRoutePropagation: false,
        routes: [],
      },
    });
    summary.resources.push(`Route Table: ${rtName}`);
  }

  // VNet
  const vnetDependsOn = [];
  const subnetDefs = enabledSubnets.map(subnet => {
    const subnetProps = { addressPrefix: subnet.cidr };

    if (subnet.nsg) {
      const nsgName = names[`nsg-${subnet.id}`] || `${subnet.name || subnet.purpose}-nsg`;
      subnetProps.networkSecurityGroup = {
        id: `[resourceId('Microsoft.Network/networkSecurityGroups', '${nsgName}')]`,
      };
      vnetDependsOn.push(`[resourceId('Microsoft.Network/networkSecurityGroups', '${nsgName}')]`);
    }

    if (subnet.routeTable) {
      const rtName = names[`rt-${subnet.id}`] || `${subnet.name || subnet.purpose}-rt`;
      subnetProps.routeTable = {
        id: `[resourceId('Microsoft.Network/routeTables', '${rtName}')]`,
      };
      vnetDependsOn.push(`[resourceId('Microsoft.Network/routeTables', '${rtName}')]`);
    }

    return {
      name: subnet.name || subnet.purpose,
      properties: subnetProps,
    };
  });

  const vnetResource = {
    type: 'Microsoft.Network/virtualNetworks',
    apiVersion: '2023-11-01',
    name: vnetName,
    location: '[parameters(\'location\')]',
    tags: '[parameters(\'tags\')]',
    dependsOn: vnetDependsOn,
    properties: {
      addressSpace: {
        addressPrefixes: cfg.hubVnet?.addressSpaces || ['10.0.0.0/16'],
      },
      subnets: subnetDefs,
    },
  };
  template.resources.push(vnetResource);
  summary.resources.push(`VNet: ${vnetName}`);

  // Bastion PIP + Bastion Host
  if (conn.bastion?.enabled) {
    const bastionName = names.bastion || conn.bastion.name || 'bastion';
    const pipName = `${bastionName}-pip`;

    template.resources.push({
      type: 'Microsoft.Network/publicIPAddresses',
      apiVersion: '2023-11-01',
      name: pipName,
      location: '[parameters(\'location\')]',
      tags: '[parameters(\'tags\')]',
      sku: { name: 'Standard' },
      properties: { publicIPAllocationMethod: 'Static' },
    });

    template.resources.push({
      type: 'Microsoft.Network/bastionHosts',
      apiVersion: '2023-11-01',
      name: bastionName,
      location: '[parameters(\'location\')]',
      tags: '[parameters(\'tags\')]',
      sku: { name: conn.bastion.sku || 'Standard' },
      dependsOn: [
        `[resourceId('Microsoft.Network/publicIPAddresses', '${pipName}')]`,
        `[resourceId('Microsoft.Network/virtualNetworks', '${vnetName}')]`,
      ],
      properties: {
        ipConfigurations: [{
          name: 'bastionIpConfig',
          properties: {
            subnet: {
              id: `[resourceId('Microsoft.Network/virtualNetworks/subnets', '${vnetName}', 'AzureBastionSubnet')]`,
            },
            publicIPAddress: {
              id: `[resourceId('Microsoft.Network/publicIPAddresses', '${pipName}')]`,
            },
          },
        }],
      },
    });
    summary.resources.push(`Bastion: ${bastionName}`);
  }

  // ER Gateway PIP + Gateway
  if (conn.expressRouteGateway?.enabled) {
    const gwName = names.erGateway || conn.expressRouteGateway.name || 'er-gateway';
    const pipName = `${gwName}-pip`;

    template.resources.push({
      type: 'Microsoft.Network/publicIPAddresses',
      apiVersion: '2023-11-01',
      name: pipName,
      location: '[parameters(\'location\')]',
      tags: '[parameters(\'tags\')]',
      sku: { name: 'Standard' },
      properties: { publicIPAllocationMethod: 'Static' },
    });

    template.resources.push({
      type: 'Microsoft.Network/virtualNetworkGateways',
      apiVersion: '2023-11-01',
      name: gwName,
      location: '[parameters(\'location\')]',
      tags: '[parameters(\'tags\')]',
      dependsOn: [
        `[resourceId('Microsoft.Network/publicIPAddresses', '${pipName}')]`,
        `[resourceId('Microsoft.Network/virtualNetworks', '${vnetName}')]`,
      ],
      properties: {
        gatewayType: 'ExpressRoute',
        sku: {
          name: conn.expressRouteGateway.sku || 'ErGw3AZ',
          tier: conn.expressRouteGateway.sku || 'ErGw3AZ',
        },
        ipConfigurations: [{
          name: 'default',
          properties: {
            subnet: {
              id: `[resourceId('Microsoft.Network/virtualNetworks/subnets', '${vnetName}', 'GatewaySubnet')]`,
            },
            publicIPAddress: {
              id: `[resourceId('Microsoft.Network/publicIPAddresses', '${pipName}')]`,
            },
          },
        }],
      },
    });
    summary.resources.push(`ER Gateway: ${gwName}`);
  }

  // ER Connection
  if (conn.expressRouteConnection?.enabled && conn.expressRouteGateway?.enabled) {
    const connName = names.erConnection || conn.expressRouteConnection.name || 'er-connection';
    const gwName = names.erGateway || conn.expressRouteGateway.name || 'er-gateway';

    const connProps = {
      connectionType: 'ExpressRoute',
      virtualNetworkGateway1: {
        id: `[resourceId('Microsoft.Network/virtualNetworkGateways', '${gwName}')]`,
      },
    };

    if (conn.expressRouteConnection.circuitResourceId) {
      connProps.peer = { id: conn.expressRouteConnection.circuitResourceId };
    }
    if (conn.expressRouteConnection.authorizationKey) {
      connProps.authorizationKey = conn.expressRouteConnection.authorizationKey;
    }

    template.resources.push({
      type: 'Microsoft.Network/connections',
      apiVersion: '2023-11-01',
      name: connName,
      location: '[parameters(\'location\')]',
      tags: '[parameters(\'tags\')]',
      dependsOn: [
        `[resourceId('Microsoft.Network/virtualNetworkGateways', '${gwName}')]`,
      ],
      properties: connProps,
    });
    summary.resources.push('ER Connection');
  }

  // Firewall
  if (conn.firewall?.enabled) {
    const fwName = names.firewall || conn.firewall.name || 'azure-firewall';
    const pipName = `${fwName}-pip`;
    const policyName = names.firewallPolicy || conn.firewall.policyName || `${fwName}-policy`;

    template.resources.push({
      type: 'Microsoft.Network/publicIPAddresses',
      apiVersion: '2023-11-01',
      name: pipName,
      location: '[parameters(\'location\')]',
      tags: '[parameters(\'tags\')]',
      sku: { name: 'Standard' },
      properties: { publicIPAllocationMethod: 'Static' },
    });

    template.resources.push({
      type: 'Microsoft.Network/firewallPolicies',
      apiVersion: '2023-11-01',
      name: policyName,
      location: '[parameters(\'location\')]',
      tags: '[parameters(\'tags\')]',
      properties: {
        threatIntelMode: conn.firewall.threatIntelMode || 'Alert',
        sku: { tier: conn.firewall.sku || 'Standard' },
      },
    });

    template.resources.push({
      type: 'Microsoft.Network/azureFirewalls',
      apiVersion: '2023-11-01',
      name: fwName,
      location: '[parameters(\'location\')]',
      tags: '[parameters(\'tags\')]',
      dependsOn: [
        `[resourceId('Microsoft.Network/publicIPAddresses', '${pipName}')]`,
        `[resourceId('Microsoft.Network/virtualNetworks', '${vnetName}')]`,
        `[resourceId('Microsoft.Network/firewallPolicies', '${policyName}')]`,
      ],
      properties: {
        sku: {
          name: 'AZFW_VNet',
          tier: conn.firewall.sku || 'Standard',
        },
        firewallPolicy: {
          id: `[resourceId('Microsoft.Network/firewallPolicies', '${policyName}')]`,
        },
        ipConfigurations: [{
          name: 'firewallIpConfig',
          properties: {
            subnet: {
              id: `[resourceId('Microsoft.Network/virtualNetworks/subnets', '${vnetName}', 'AzureFirewallSubnet')]`,
            },
            publicIPAddress: {
              id: `[resourceId('Microsoft.Network/publicIPAddresses', '${pipName}')]`,
            },
          },
        }],
      },
    });
    summary.resources.push(`Firewall: ${fwName}`);
  }

  // Outputs
  template.outputs.vnetId = {
    type: 'string',
    value: `[resourceId('Microsoft.Network/virtualNetworks', '${vnetName}')]`,
  };

  return { template, summary };
}

