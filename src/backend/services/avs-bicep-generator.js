// ============================================================================
// Infrastructure Deployment Generator — AVS Bicep / ARM Generator
// ============================================================================
// Generates Bicep and ARM JSON templates for AVS private cloud resources.
// ============================================================================

// ---------------------------------------------------------------------------
// Generate Bicep template
// ---------------------------------------------------------------------------

export function generateAvsBicep(cfg, names = {}) {
  const lines = [];
  const summary = { resources: [] };

  const region = cfg.region || 'eastus2';
  const pc = cfg.privateCloud || {};
  const conn = cfg.connectivity || {};
  const pcName = names.privateCloud || pc.name || 'avs-private-cloud';

  lines.push('// ============================================================================');
  lines.push('// AVS Private Cloud — Auto-generated by Infrastructure Deployment Generator');
  lines.push(`// Region: ${region}`);
  lines.push(`// Generated: ${new Date().toISOString()}`);
  lines.push('// ============================================================================');
  lines.push('');
  lines.push("targetScope = 'resourceGroup'");
  lines.push('');

  // Parameters
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('// Parameters');
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('');
  lines.push(`param location string = '${region}'`);
  lines.push('param tags object = {}');
  lines.push('');

  // Variables
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('// Variables');
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('');
  lines.push(`var privateCloudName = '${pcName}'`);
  lines.push('');

  // Private Cloud
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('// AVS Private Cloud');
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('');
  lines.push("resource privateCloud 'Microsoft.AVS/privateClouds@2023-09-01' = {");
  lines.push('  name: privateCloudName');
  lines.push('  location: location');
  lines.push('  tags: tags');
  lines.push('  sku: {');
  lines.push(`    name: '${pc.sku || 'AV36P'}'`);
  lines.push('  }');
  lines.push('  properties: {');
  lines.push(`    networkBlock: '${pc.addressBlock || '10.0.0.0/22'}'`);
  lines.push('    managementCluster: {');
  lines.push(`      clusterSize: ${pc.nodeCount || 3}`);
  lines.push('    }');
  lines.push('    internet: \'Disabled\'');
  lines.push('  }');
  lines.push('}');
  lines.push('');
  summary.resources.push(`Private Cloud: ${pcName} (${pc.sku || 'AV36P'} x${pc.nodeCount || 3})`);

  // Secondary clusters
  for (const cluster of (pc.secondaryClusters || [])) {
    const clusterName = cluster.name || 'cluster-2';
    lines.push(`resource ${clusterName.replace(/[^a-zA-Z0-9]/g, '')} 'Microsoft.AVS/privateClouds/clusters@2023-09-01' = {`);
    lines.push('  parent: privateCloud');
    lines.push(`  name: '${clusterName}'`);
    lines.push('  sku: {');
    lines.push(`    name: '${cluster.sku || pc.sku || 'AV36P'}'`);
    lines.push('  }');
    lines.push('  properties: {');
    lines.push(`    clusterSize: ${cluster.nodeCount || 3}`);
    lines.push('  }');
    lines.push('}');
    lines.push('');
    summary.resources.push(`Cluster: ${clusterName} (${cluster.sku || pc.sku} x${cluster.nodeCount || 3})`);
  }

  // ExpressRoute authorization
  if (conn.avsExpressRoute?.enabled) {
    const authName = names.erAuth || conn.avsExpressRoute.authorizationKeyName || 'avs-er-auth';
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('// ExpressRoute Authorization');
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('');
    lines.push("resource erAuth 'Microsoft.AVS/privateClouds/authorizations@2023-09-01' = {");
    lines.push('  parent: privateCloud');
    lines.push(`  name: '${authName}'`);
    lines.push('}');
    lines.push('');
    summary.resources.push(`ER Authorization: ${authName}`);
  }

  // Hub connection (ER connection from hub gateway to AVS circuit)
  if (conn.hubConnection?.enabled && conn.hubConnection.hubGatewayResourceId) {
    const connName = names.hubConnection || conn.hubConnection.connectionName || 'avs-hub-connection';
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('// Hub VNet Connection (ER Gateway → AVS Circuit)');
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('');
    lines.push("resource hubConnection 'Microsoft.Network/connections@2023-11-01' = {");
    lines.push(`  name: '${connName}'`);
    lines.push('  location: location');
    lines.push('  tags: tags');
    lines.push('  properties: {');
    lines.push("    connectionType: 'ExpressRoute'");
    lines.push('    virtualNetworkGateway1: {');
    lines.push(`      id: '${conn.hubConnection.hubGatewayResourceId}'`);
    lines.push('    }');
    lines.push('    peer: {');
    lines.push('      id: privateCloud.properties.circuit.expressRouteID');
    lines.push('    }');
    if (conn.avsExpressRoute?.enabled) {
      lines.push('    authorizationKey: erAuth.properties.expressRouteAuthorizationKey');
    }
    lines.push('  }');
    lines.push('}');
    lines.push('');
    summary.resources.push('Hub ER Connection');
  }

  // Global Reach
  if (conn.globalReach?.enabled && conn.globalReach.onPremCircuitResourceId) {
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('// ExpressRoute Global Reach');
    lines.push('// ---------------------------------------------------------------------------');
    lines.push('');
    lines.push("resource globalReach 'Microsoft.AVS/privateClouds/globalReachConnections@2023-09-01' = {");
    lines.push('  parent: privateCloud');
    lines.push("  name: 'avs-global-reach'");
    lines.push('  properties: {');
    lines.push(`    peerExpressRouteCircuit: '${conn.globalReach.onPremCircuitResourceId}'`);
    if (conn.globalReach.peeringAddressPrefix) {
      lines.push(`    authorizationKey: '${conn.globalReach.peeringAddressPrefix}'`);
    }
    lines.push('  }');
    lines.push('}');
    lines.push('');
    summary.resources.push('Global Reach');
  }

  // Outputs
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('// Outputs');
  lines.push('// ---------------------------------------------------------------------------');
  lines.push('');
  lines.push('output privateCloudId string = privateCloud.id');
  lines.push('output privateCloudName string = privateCloud.name');
  lines.push('output managementCidr string = privateCloud.properties.networkBlock');
  lines.push('');

  return { bicep: lines.join('\n'), summary };
}

// ---------------------------------------------------------------------------
// Generate ARM JSON template
// ---------------------------------------------------------------------------

export function generateAvsArmJson(cfg, names = {}) {
  const region = cfg.region || 'eastus2';
  const pc = cfg.privateCloud || {};
  const conn = cfg.connectivity || {};
  const pcName = names.privateCloud || pc.name || 'avs-private-cloud';
  const summary = { resources: [] };

  const template = {
    $schema: 'https://schema.management.azure.com/schemas/2019-04-01/deploymentTemplate.json#',
    contentVersion: '1.0.0.0',
    metadata: {
      _generator: {
        name: 'Infrastructure Deployment Generator',
        version: '1.0.0',
        timestamp: new Date().toISOString(),
      },
    },
    parameters: {
      location: { type: 'string', defaultValue: region },
      tags: { type: 'object', defaultValue: {} },
    },
    variables: {},
    resources: [],
    outputs: {},
  };

  // Private Cloud
  template.resources.push({
    type: 'Microsoft.AVS/privateClouds',
    apiVersion: '2023-09-01',
    name: pcName,
    location: '[parameters(\'location\')]',
    tags: '[parameters(\'tags\')]',
    sku: { name: pc.sku || 'AV36P' },
    properties: {
      networkBlock: pc.addressBlock || '10.0.0.0/22',
      managementCluster: { clusterSize: pc.nodeCount || 3 },
      internet: 'Disabled',
    },
  });
  summary.resources.push(`Private Cloud: ${pcName}`);

  // ER Authorization
  if (conn.avsExpressRoute?.enabled) {
    const authName = names.erAuth || conn.avsExpressRoute.authorizationKeyName || 'avs-er-auth';
    template.resources.push({
      type: 'Microsoft.AVS/privateClouds/authorizations',
      apiVersion: '2023-09-01',
      name: `${pcName}/${authName}`,
      dependsOn: [`[resourceId('Microsoft.AVS/privateClouds', '${pcName}')]`],
      properties: {},
    });
    summary.resources.push(`ER Authorization: ${authName}`);
  }

  // Outputs
  template.outputs.privateCloudId = {
    type: 'string',
    value: `[resourceId('Microsoft.AVS/privateClouds', '${pcName}')]`,
  };

  return { template, summary };
}

export default { generateAvsBicep, generateAvsArmJson };
