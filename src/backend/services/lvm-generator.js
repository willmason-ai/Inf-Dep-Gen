// ============================================================================
// Infrastructure Deployment Generator — LVM Script Generator
// ============================================================================
// Generates idempotent LVM configuration scripts for ODB (Linux) servers.
// Creates PVs and VGs from attached Azure data disks.
// Matches reference lab scripts structure.
// ============================================================================

import { getLunDeviceMap } from '../models/server-spec.js';

// ---------------------------------------------------------------------------
// Validate VG name — must be valid for LVM (no brackets, spaces, etc.)
// ---------------------------------------------------------------------------
function validateVgName(name) {
  if (!name) return 'VG name is empty';
  if (/[\[\]\s]/.test(name)) {
    return `VG name "${name}" contains invalid characters (brackets or spaces). The [env]vg pattern was not resolved — check that the spec file has an LVM configuration reference block.`;
  }
  if (!/^[a-zA-Z0-9._-]+$/.test(name)) {
    return `VG name "${name}" contains characters not valid for LVM.`;
  }
  return null;
}

// ---------------------------------------------------------------------------
// Main generator: produce LVM bash script from a server spec
// ---------------------------------------------------------------------------
export function generateLvmScript(spec) {
  if (spec.serverType !== 'odb') {
    return {
      error: `LVM scripts are only generated for ODB (Linux) servers. ${spec.hostname} is a ${spec.serverType} server.`,
    };
  }

  if (!spec.volumeGroups || spec.volumeGroups.length === 0) {
    return {
      error: `No volume groups defined for ${spec.hostname}`,
    };
  }

  // Validate all VG names before generating any script
  for (const vg of spec.volumeGroups) {
    const vgError = validateVgName(vg.name);
    if (vgError) {
      return { error: vgError };
    }
  }

  // Check for non-numeric diskCount values
  const warnings = [];
  for (const vg of spec.volumeGroups) {
    if (typeof vg.diskCount !== 'number') {
      warnings.push(`VolumeGroup "${vg.name}": diskCount is "${vg.diskCount}" — cannot generate LVM commands for this VG.`);
    }
  }

  // If ALL VGs have non-numeric disk counts, we can't generate anything useful
  const usableVgs = spec.volumeGroups.filter(vg => typeof vg.diskCount === 'number' && vg.diskCount > 0);
  if (usableVgs.length === 0) {
    return {
      error: `No volume groups with valid disk counts for ${spec.hostname}. ${warnings.join(' ')}`,
    };
  }

  const lines = [];
  let currentLun = 0;

  // Header
  lines.push('#!/bin/bash');
  lines.push('# ============================================================================');
  lines.push(`# LVM Setup for ${spec.hostname}`);
  lines.push(`# Server: ${spec.role}`);
  lines.push(`# OS: ${spec.os || 'RHEL-8'}`);
  lines.push('# ============================================================================');
  lines.push(`# Generated by Infrastructure Deployment Generator on ${new Date().toISOString()}`);
  lines.push('#');
  lines.push('# Volume Group Layout:');

  // Document the VG layout in the header
  for (const vg of spec.volumeGroups) {
    if (typeof vg.diskCount !== 'number') {
      lines.push(`#   ${vg.name.padEnd(14)} TBD disk(s)  (SKIPPED — disk count not resolved)`);
      continue;
    }
    const devices = getLunDeviceMap(currentLun, vg.diskCount);
    const lunRange = vg.diskCount === 1
      ? `LUN ${currentLun}`
      : `LUNs ${currentLun}-${currentLun + vg.diskCount - 1}`;
    lines.push(`#   ${vg.name.padEnd(14)} ${vg.diskCount} disk(s)  ${lunRange.padEnd(12)}  ${devices.join(', ')}`);
    currentLun += vg.diskCount;
  }

  if (warnings.length > 0) {
    lines.push('#');
    lines.push('# WARNINGS:');
    for (const w of warnings) {
      lines.push(`#   ${w}`);
    }
  }

  lines.push('# ============================================================================');
  lines.push('');
  lines.push('set -e');
  lines.push('');
  lines.push('# Verify running as root');
  lines.push('if [ "$(id -u)" -ne 0 ]; then');
  lines.push('    echo "ERROR: This script must be run as root"');
  lines.push('    exit 1');
  lines.push('fi');
  lines.push('');

  // Reset LUN counter for the actual script body
  currentLun = 0;

  for (const vg of spec.volumeGroups) {
    // Skip VGs with non-numeric disk counts
    if (typeof vg.diskCount !== 'number') {
      lines.push(`# SKIPPED: ${vg.name} — disk count is "${vg.diskCount}" (not resolved)`);
      lines.push('');
      continue;
    }

    const devices = getLunDeviceMap(currentLun, vg.diskCount);

    lines.push('# ============================================================================');
    lines.push(`# Volume Group: ${vg.name}`);
    lines.push(`# Disks: ${vg.diskCount}  |  Size per disk: ${vg.sizeGB || '?'} GB  |  IOPS: ${vg.iops || '?'}  |  Throughput: ${vg.throughputMBs || '?'} MB/s`);
    lines.push('# ============================================================================');
    lines.push('');

    // Define disk variables
    for (let i = 0; i < vg.diskCount; i++) {
      const diskVar = `DISK_${currentLun + i}`;
      lines.push(`${diskVar}="${devices[i]}"   # LUN ${currentLun + i} — ${vg.name} disk ${i + 1}`);
    }
    lines.push('');

    // Collect disks into array
    const arrayName = `VG_${vg.name.toUpperCase()}_DISKS`;
    const diskVarRefs = [];
    for (let i = 0; i < vg.diskCount; i++) {
      diskVarRefs.push(`"$DISK_${currentLun + i}"`);
    }
    lines.push(`${arrayName}=(${diskVarRefs.join(' ')})`);
    lines.push('');

    // Verify disks exist
    lines.push(`echo "--- Verifying disks for ${vg.name} ---"`);
    lines.push(`for disk in "\${${arrayName}[@]}"; do`);
    lines.push('    if [ ! -b "$disk" ]; then');
    lines.push(`        echo "ERROR: Disk $disk not found. Ensure all ${vg.diskCount} data disks for ${vg.name} are attached."`)
    lines.push('        exit 1');
    lines.push('    fi');
    lines.push('done');
    lines.push(`echo "  All ${vg.diskCount} disk(s) for ${vg.name} verified."`);
    lines.push('');

    // Create PVs idempotently
    lines.push(`echo "--- Creating Physical Volumes for ${vg.name} ---"`);
    lines.push(`for disk in "\${${arrayName}[@]}"; do`);
    lines.push('    if pvs "$disk" &>/dev/null; then');
    lines.push('        echo "  PV $disk already exists — skipping"');
    lines.push('    else');
    lines.push('        echo "  Creating PV on $disk"');
    lines.push('        pvcreate "$disk"');
    lines.push('    fi');
    lines.push('done');
    lines.push('');

    // Create VG idempotently
    lines.push(`echo "--- Creating Volume Group: ${vg.name} ---"`);
    lines.push(`if vgs "${vg.name}" &>/dev/null; then`);
    lines.push(`    echo "  VG ${vg.name} already exists — skipping"`);
    lines.push('else');
    lines.push(`    echo "  Creating VG ${vg.name}"`);
    lines.push(`    vgcreate "${vg.name}" "\${${arrayName}[@]}"`);
    lines.push('fi');
    lines.push('');

    // Display VG info
    lines.push(`vgdisplay "${vg.name}"`);
    lines.push('');

    currentLun += vg.diskCount;
  }

  // Summary
  lines.push('# ============================================================================');
  lines.push('# Summary');
  lines.push('# ============================================================================');
  lines.push('echo ""');
  lines.push('echo "=== Physical Volumes ==="');
  lines.push('pvs');
  lines.push('echo ""');
  lines.push('echo "=== Volume Groups ==="');
  lines.push('vgs');
  lines.push('echo ""');
  lines.push(`echo "LVM setup for ${spec.hostname} complete."`);
  lines.push('echo ""');
  lines.push('echo "Next steps:"');
  lines.push('echo "  1. Create Logical Volumes (lvcreate)"');
  lines.push('echo "  2. Create filesystems (mkfs.xfs or mkfs.ext4)"');
  lines.push('echo "  3. Create mount points and update /etc/fstab"');
  lines.push('');

  const script = lines.join('\n');

  const summary = {
    hostname: spec.hostname,
    serverType: spec.serverType,
    volumeGroups: spec.volumeGroups.map(vg => ({
      name: vg.name,
      diskCount: vg.diskCount,
      totalSizeGB: vg.totalSizeGB || (typeof vg.diskCount === 'number' && vg.sizeGB ? vg.sizeGB * vg.diskCount : 'TBD'),
    })),
    totalDisks: spec.volumeGroups.reduce((sum, vg) => sum + (typeof vg.diskCount === 'number' ? vg.diskCount : 0), 0),
    warnings: warnings.length > 0 ? warnings : undefined,
  };

  return { script, summary, warnings };
}

export default { generateLvmScript };
